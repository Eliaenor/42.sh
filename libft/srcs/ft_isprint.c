#include "../includes/libft.h"

int		ft_isprint(int c)
{
	if (ft_isalnum(c) != 0)
		return (1);
	if ((' ' <= c && c <= '/') || (':' <= c && c <= '@') ||
		('[' <= c && c <= '`') || ('{' <= c && c <= '~'))
		return (1);
	else
		return (0);
}
